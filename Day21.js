// This Property represents Euler's Number, e = 2.178.....

let a = Math.E;
let b = a.toFixed(7);
console.log(b); // Output is 2.7182818

// This Property represents Euler's Number, pi = 3.14.....

let a = Math.PI;
let b = a.toFixed(7);
console.log(b); // Output 3.1415927

